<script>
  import NavHeader from './../components/NavHeader'
  import NavFooter from './../components/NavFooter'
  import NavBread from './../components/NavBread'
  import Modal from './../components/Modal'
  import {currency} from './../util/currency'
  import { Validator } from 'vee-validate';
  import axios from 'axios';
  const validator = new Validator();
  axios.defaults.withCredentials = true;
  export default{
    data(){
      return{
        limit:3,
        checkIndex:0,
        selectedAddrId:'',
        addressList:[],
        isMdShow:false,
        mdShow: false,
        userName: '',
        streetName: '',
        postCode: '',
        tel: '',
        isDefault: false,
        overLayFlag: false,
        msgShow: false,
        msg: '',
        addressId:''
      }
    },
    mounted(){
      this.init();
    },
    computed:{
      addressListFilter(){
        return this.addressList.slice(0,this.limit);
      }
    },
    components:{
      NavHeader,
      NavFooter,
      NavBread,
      Modal
    },
    methods:{
      init(){
        axios.get("http://localhost:3000/users/addressList").then((response)=>{
          let res = response.data;
          this.addressList = res.result;
          console.log(res.result);
          this.selectedAddrId = this.addressList[0].addressId;
        });
      },
      expand(){
        if(this.limit==3){
          this.limit = this.addressList.length;
        }else{
          this.limit = 3;
        }
      },
      setDefault(addressId){
        axios.post("http://localhost:3000/users/setDefault",{
          addressId:addressId
        }).then((response)=>{
          let res = response.data;
          if(res.status=='0'){
            console.log("set default");
            this.init();
          }
        })
      },
      closeModal(){
        this.isMdShow = false;
      },
      delAddressConfirm(addressId){
        this.isMdShow = true;
        this.addressId = addressId;
      },
      delAddress(){
        axios.post("http://localhost:3000/users/delAddress",{
          addressId:this.addressId
        }).then((response)=>{
          let res = response.data;
          if(res.status=="0"){
            console.log("del suc");
            this.isMdShow = false;
            this.init();
          }else{
            this.isMdShow = false;
            alert(res.msg)
          }
        })
      },
      addressAdd () {
          this.$validator.validateAll().then((e)=>{
            if (e) {
              let addressId = Math.round(Math.random()*10000000000);
              axios.post('/users/addressAdd', {
                'addressId': addressId,
                'userName': this.userName,
                'streetName': this.streetName,
                'postCode': this.postCode,
                'tel': this.tel,
                'isDefault': this.isDefault
              }).then((res) => {
                let result = res.data;
                if (result.status === 0) {
                  this.closeAddModal();
                  this.init()
                } else {
                  this.msg = result.msg;
                  this.overLayFlag = true;
                  this.msgShow = true
                }
              })
            } else {
                return false
            }
          });
      },
      closeAddModal () {
        this.mdShow = false;
        this.overLayFlag = false;
        this.addressId = '';
        this.userName = '';
        this.postCode = '';
        this.streetName = '';
        this.tel = '';
        this.isDefault = false
      },
      msgClose () {
        this.msg = '';
        this.msgShow = false;
        this.overLayFlag = false
      },
      closeOverLay () {
        this.msgClose();
        this.closeAddModal()
      }
    }
  }
</script>
